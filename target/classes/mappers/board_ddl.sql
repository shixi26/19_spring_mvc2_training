CREATE DATABASE SPRING_MVC2_BOARD;
		
USE SPRING_MVC2_BOARD;

CREATE TABLE T_BOARD(
	NUM 		INT NOT NULL AUTO_INCREMENT PRIMARY KEY,
    WRITER 		VARCHAR(50),
    EMAIL 		VARCHAR(50),
    SUBJECT 	VARCHAR(50),
    PASSWORD 	VARCHAR(20),
    REG_DATE 	DATE,
    READ_COUNT  INT,
   	CONTENT 	VARCHAR(200)
);

SELECT * FROM T_BOARD;